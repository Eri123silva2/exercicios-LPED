//2.	Implemente um programa capaz de imprimir os números de 3 e 3, de 0 até 30

console.log("Usando for:")
for (i = 0; i <= 30; i = i + 3) {
    console.log(i)
}

///////////////////////////////////////

console.log("Usando while:")

i = 0

while (i <= 30) {
    console.log(i)
    i = i + 3
}
console.log("Fim do programa")