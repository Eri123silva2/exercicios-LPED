//	Implemente um programa capaz de multiplicar dois números e verificar se o resultado é um número negativo;
var n1 = 15;
var n2 = -4;
var resultado = n1 * n2;

if(resultado > 0){
    console.log("O número é positivo");
}else {
    console.log("O número é negativo");
}
