// cria um array vazio
var valores = []

// inicializa o array
valores[0] = 'b'

// valores
// índice	->	valor
// 0		->	'b'


valores[1] = '-'

// valores
// índice	->	valor
// 0		->	'b'
// 1		->	'-'


valores[2] = '*'

// valores
// índice	->	valor
// 0		->	'b'
// 1		->	'-'
// 2		->	'*'


valores[3] = 7

// valores
// índice	->	valor
// 0		->	'b'
// 1		->	'-'
// 2		->	'*'
// 3		->	7

// imprime todos os elementos do array
// notem a ordem invertida
console.log(valores[0])
console.log(valores[1])
console.log(valores[2])
console.log(valores[3])




