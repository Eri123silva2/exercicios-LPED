// Exemplo 1.3 - Edição de elementos de um array.


var arrayDeNumeros = [ // vetor de três linhas por quatro colunas
    [1,  8,  2,  0],
    [7,  3, -2, 14],
    [4,  1,  4, -8]
]


arrayDeNumeros[1][2] = 7 // altera o valor de -2 para 7
arrayDeNumeros[0][0] = 4 // altera o valor de 1 para 4
arrayDeNumeros[2][3] = -1 // altera o valor de -8 para -1

